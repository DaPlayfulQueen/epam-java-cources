package com.epam.university.java.core.task038;

/**
 * Stores data about node.
 */
public interface Node {

    /**
     * Get id of node
     *
     * @return id value
     */
    int getId();

    /**
     * Get x coordinate of node
     *
     * @return x coordinate value
     */
    int getX();

    /**
     * Get y coordinate of node
     *
     * @return y coordinate value
     */
    int getY();
}
